from ultralytics import YOLO
import torch
import os

if __name__ == '__main__':
    # 1. ตั้งค่า Environment ป้องกัน Library ขัดแย้งกันบน Windows
    os.environ['KMP_DUPLICATE_LIB_OK']='True'
    
    # 2. โหลดโมเดล YOLO11s (เวอร์ชัน Small) เพื่อใช้ทำ Transfer Learning
    model = YOLO('yolo11s.pt') 

    # 3. เริ่มการฝึกสอน (Training) พร้อมปรับจูนค่าเพื่อความเสถียรของเครื่อง
    model.train(
        data='autogen_data.yaml',      # ไฟล์ที่ระบุตำแหน่ง Dataset และ Class ทั้ง 6
        epochs=100,                    # จำนวนรอบสูงสุดในการเทรน
        imgsz=416,                     # ลดขนาดภาพเพื่อประหยัด VRAM และลดความร้อน GPU
        batch=8,                       # จำนวนภาพต่อรอบ (Batch Size) ให้เหมาะสมกับหน่วยความจำ
        
        # --- ระบบควบคุมการทำงาน ---
        patience=10,                   # Early Stopping: หยุดเทรนหากความแม่นยำไม่ดีขึ้นใน 10 รอบ
        
        # --- การตั้งค่าเพื่อความเสถียร (Hardware Safety) ---
        amp=False,                     # ปิดระบบเร่งความเร็วเพื่อป้องกันแรงดันไฟไม่นิ่ง (PSU Safety)
        workers=0,                     # ใช้ CPU ตัวหลักทำงานแบบลำดับเพื่อลดภาระโดยรวมของระบบ
        device=0,                      # บังคับใช้การ์ดจอ (GPU) ตัวแรกในการประมวลผล
        
        # --- การเพิ่มความหลากหลายให้ข้อมูล (Data Augmentation) ---
        mosaic=1.0,                    # เทคนิคผสมภาพ 4 รูปเข้าด้วยกัน ช่วยให้ตรวจจับวัตถุขนาดเล็กได้ดี
        mixup=0.1,                     # เทคนิคซ้อนทับภาพ ช่วยลดการทายผิดเมื่อวัตถุบังกัน
        scale=0.5,                     # สุ่มย่อ-ขยายขนาดวัตถุ เพิ่มความทนทานต่อระยะใกล้-ไกล
        fliplr=0.5,                    # สุ่มพลิกภาพซ้าย-ขวา ให้โมเดลฉลาดขึ้นในทุกทิศทาง
        
        # --- การจัดการไฟล์และหน่วยความจำ ---
        project='runs/detect',         # โฟลเดอร์เก็บผลลัพธ์การทดลอง
        name='SuperNitro_AntiCrash_S1', # ชื่อรุ่น (Version) ของการเทรนครั้งนี้
        exist_ok=True,                 # อนุญาตให้บันทึกทับโฟลเดอร์เดิมได้
        cache=False                    # ไม่เก็บข้อมูลใน RAM ทั้งหมดเพื่อป้องกัน Memory Full
    )